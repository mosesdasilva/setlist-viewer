from __future__ import annotations

import json
import os
import shutil
import tempfile
import unittest
from pathlib import Path
from subprocess import run
from unittest.mock import patch

from tools import build as build_module


ROOT = Path(__file__).resolve().parents[1]
BUILD = ROOT / "tools" / "build.py"


def read_generated(path: Path):
    source = path.read_text(encoding="utf-8")
    prefix = "window.SETLIST_SONGS = "
    return source, json.loads(source[source.index(prefix) + len(prefix) :].removesuffix(";\n"))


class BuildTest(unittest.TestCase):
    def run_build(self, root: Path, *arguments: str):
        return run(
            ["/usr/bin/python3", str(BUILD), *arguments],
            cwd=root,
            check=False,
            capture_output=True,
            text=True,
        )

    def copy_inputs(self, destination: Path) -> None:
        shutil.copytree(ROOT / "charts", destination / "charts")
        (destination / "src").mkdir()
        for name in ("index.html", "styles.css", "script.js", "song-data.js"):
            shutil.copy(ROOT / "src" / name, destination / "src" / name)

    def test_build_generates_deterministic_self_contained_portable_viewer(self):
        with tempfile.TemporaryDirectory() as directory:
            root = Path(directory)
            self.copy_inputs(root)
            prototype = root / "setlist-viewer-v1.html"
            prototype.write_text("prior prototype\n", encoding="utf-8")

            first = self.run_build(root)
            portable = root / "setlist-viewer-portable.html"
            first_bytes = portable.read_bytes()
            second = self.run_build(root)

            self.assertEqual(0, first.returncode, first.stdout + first.stderr)
            self.assertEqual(0, second.returncode, second.stdout + second.stderr)
            self.assertEqual(first_bytes, portable.read_bytes())
            self.assertEqual("prior prototype\n", prototype.read_text(encoding="utf-8"))

            html = first_bytes.decode("utf-8")
            self.assertIn("GENERATED by python3 tools/build.py", html)
            for source in (
                "src/index.html", "src/styles.css", "src/script.js",
                "charts/catalog.json", "charts/*.chart", "src/song-data.js",
            ):
                self.assertIn(source, html)
            self.assertIn("DO NOT EDIT", html)
            self.assertNotIn('<link rel="stylesheet"', html)
            self.assertNotIn('<script src=', html)
            self.assertNotIn('type="module"', html)
            self.assertNotIn("fetch(", html)
            self.assertIn((root / "src" / "styles.css").read_text(encoding="utf-8"), html)
            self.assertIn((root / "src" / "script.js").read_text(encoding="utf-8"), html)

            _, expected_songs = read_generated(root / "src" / "chart-data.js")
            data_prefix = "window.SETLIST_SONGS = "
            embedded = html[html.index(data_prefix) + len(data_prefix) :]
            embedded = embedded[:embedded.index(";\n")]
            self.assertEqual(expected_songs, json.loads(embedded))

