    def test_replacement_uses_id_once_and_never_title(self):
        with tempfile.TemporaryDirectory() as directory:
            root = Path(directory)
            self.copy_inputs(root)
            legacy_path = root / "src" / "song-data.js"
            legacy = legacy_path.read_text(encoding="utf-8")
            legacy = legacy.replace('"id": "jesus",\n    "title": "Jesus"', '"id": "mas",\n    "title": "Different title"', 1)
            legacy = legacy.replace('"title": "El Canto de Redencion"', '"title": "Más"', 1)
            legacy_path.write_text(legacy, encoding="utf-8")
            catalog_path = root / "charts" / "catalog.json"
            catalog = json.loads(catalog_path.read_text(encoding="utf-8"))
            catalog.remove("jesus")
            catalog_path.write_text(json.dumps(catalog), encoding="utf-8")

            completed = self.run_build(root)

            self.assertEqual(0, completed.returncode, completed.stdout + completed.stderr)
            _, songs = read_generated(root / "src" / "chart-data.js")
            self.assertEqual(1, sum(song["id"] == "mas" for song in songs))
            self.assertEqual("chart", next(song for song in songs if song["id"] == "mas")["type"])
            self.assertEqual("legacy", next(song for song in songs if song["title"] == "Más")["type"])

    def test_check_detects_drift_and_never_writes(self):
        with tempfile.TemporaryDirectory() as directory:
            root = Path(directory)
            self.copy_inputs(root)
            output = root / "src" / "chart-data.js"
            output.write_text("stale\n", encoding="utf-8")

            completed = self.run_build(root, "--check")

            self.assertNotEqual(0, completed.returncode)
            self.assertEqual("stale\n", output.read_text(encoding="utf-8"))
            self.assertIn("python3 tools/build.py", completed.stdout)

            chart_path = root / "charts" / "mas.chart"
            chart_path.write_text(
                chart_path.read_text(encoding="utf-8").replace("@key D", "@key H"),
                encoding="utf-8",
            )
            invalid = self.run_build(root, "--check")
            self.assertNotEqual(0, invalid.returncode)
            self.assertEqual("stale\n", output.read_text(encoding="utf-8"))
            self.assertIn("[E014]", invalid.stdout)

